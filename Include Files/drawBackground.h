// Define a function to draw the background
void drawBackground() {
  
  // Define constants to hold the rgb values of the background
  const int BACKGROUND_R = 135;
  const int BACKGROUND_G = 206;
  const int BACKGROUND_B = 235;
  
  // Fill the graphics_window with the background color
  graphics_fill(BACKGROUND_R, BACKGROUND_G, BACKGROUND_B);
  
}